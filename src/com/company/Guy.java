package com.company;

public class Guy {
    int year;
    String name;
    float value;

    public Guy(int year, String name, float value) {
        this.year = year;
        this.name = name;
        this.value = value;
    }


    public int getYear() {
        return year;
    }

    public String getName() {
        return name;
    }

    public float getValue() {
        return value;
    }
}
